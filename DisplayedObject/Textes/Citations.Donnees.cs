		// Classe pour melanger un tableau
		public class Melangeur : IComparer
		{
			private static Random rnd;

			static Melangeur()
			{
				rnd = new Random();
			}

			public int Compare( object x, object y)
			{

				if (x.Equals(y))
					return 0;
				else
					return rnd.Next(-1, 1);
			}

		}
